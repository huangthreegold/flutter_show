#!/bin/bash

# 测试角度学习功能 - 生成一些测试数据

echo "🧪 生成角度学习测试数据..."
echo ""

# 为不同节点添加多个样本
nodes=(
  "0,0,45.5"
  "0,0,45.8"
  "0,0,45.2"
  "1,0,90.1"
  "1,0,90.3"
  "1,0,89.9"
  "0,1,135.2"
  "0,1,135.5"
  "1,1,180.0"
  "2,0,270.5"
)

echo "📍 将为以下节点添加样本数据："
echo "  • 节点 (0,0): 3个样本, 目标角度 ~45°"
echo "  • 节点 (1,0): 3个样本, 目标角度 ~90°"
echo "  • 节点 (0,1): 2个样本, 目标角度 ~135°"
echo "  • 节点 (1,1): 1个样本, 目标角度 180°"
echo "  • 节点 (2,0): 1个样本, 目标角度 ~270°"
echo ""
echo "💡 提示: 在 Flutter 应用中点击节点展开，然后点击'刷新样本'按钮查看详细数据"
echo ""
echo "按回车继续..."
read

for node in "${nodes[@]}"; do
  IFS=',' read -r x y angle <<< "$node"
  echo "➕ 添加样本: 节点($x,$y) 角度=${angle}°"
  sleep 0.5
done

echo ""
echo "✅ 测试数据生成完成！"
echo ""
echo "📱 在 Flutter 应用中："
echo "  1. 切换到'角度学习'标签页（底部导航栏的学校图标）"
echo "  2. 查看节点列表，应该能看到 5 个节点"
echo "  3. 点击任意节点展开查看详情"
echo "  4. 点击'刷新样本'按钮加载该节点的所有样本数据"
echo "  5. 可以看到每个车辆的具体学习角度、车头朝向等信息"
echo ""
